package oops_concepts;

public class AccessSpecifierDemo1_0505 
{
	public void name()
	{
		System.out.println("Name of the person is XYZ");
	}
	protected void mobile()
	{
		System.out.println("Mobile number of the person is 1322332");
	}
	private void salary()
	{
		System.out.println("Salary of the person is 900000");
	}
	void Id()
	{
		System.out.println("Id of the person is STA018");
	}
	public String name;
	protected int mobile;
}
