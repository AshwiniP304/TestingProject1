package core_java;

public class DataTypes 
{

	public static void main(String[] args)
	{
	
		//PRMITIVES DATA TYPES
		
	boolean b = true;
	System.out.println(b);
	char c = 'a';
	float f = 3.123456789f;
	System.out.println(f);
	double d = 3.1234567890123456789d;
	System.out.println(d);
	byte by = 127;
	System.out.println(by);
	short s = 32701;
	System.out.println(s);
	long l = 1234567890123456l;
	System.out.println(l);
	
		//NON PRIMITIVES
	
	String str = "Ashwini Patil";
	System.out.println("Ashwini Patil                     01");

	}

}
